use accessnewpip
go

set ansi_nulls on;
go

set quoted_identifier on;
go

create function dbo.fn_get_pipdistrict(@district nvarchar(25))
returns nvarchar(25)
with execute as caller
	begin
		declare @pip_district nvarchar(25) = right(@district, len(@district) - charindex('-', @district));
		return @pip_district
	end;